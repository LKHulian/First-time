# Variables and Print Practice
# This file is for learning Python variables and how to display them
# Learning Date: June 2026

# Example 1: Create a variable with a name
# A variable is a container that stores information
name = "Yosep"

# Print the variable to display it on the screen
print(name)

# Example 2: Create more variables with different information
age = 25
city = "Indonesia"

# Print each variable
print(age)
print(city)

# Example 3: Print multiple variables in one line
# Use comma (,) to separate variables
print(name, age, city)

# Example 4: Print text with variables together
# Use + to combine text and variables
print("My name is " + name)
print("I am " + str(age) + " years old")
print("I live in " + city)

# Example 5: Using f-string (modern way in Python)
# This is an easy way to combine text and variables
print(f"Hello! My name is {name}")
print(f"I am {age} years old")
print(f"I live in {city}")

# Example 6: Different types of variables
# String (text)
first_name = "Muhammad"
last_name = "Yosep"

# Integer (whole number)
phone_number = 628123456789

# Float (decimal number)
height = 170.5

# Boolean (True or False)
is_student = True

# Print all of them
print(first_name)
print(last_name)
print(phone_number)
print(height)
print(is_student)

# Example 7: Change variable value
# You can change what is stored in a variable
hobby = "Reading"
print(f"My hobby is {hobby}")

# Change the hobby variable
hobby = "Programming"
print(f"Now my hobby is {hobby}")

